const char* bytetohex(char c) {
  hex[0] = hextable[(c >> 4) & 0xf];
  hex[1] = hextable[(c >> 0) & 0xf];
  hex[2] = '\0';
  return hex;
}

char hextobyte(const char* h) {
  return (hexmap[(int)h[0]] << 4) | hexmap[(int)h[1]];
}
